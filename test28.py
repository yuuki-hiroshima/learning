# 🔹 お題（要件のみ）
# ユーザーに 2つの数字 を入力してもらう。
# 正しい入力がされるまで繰り返し再入力させる。
# 計算が成功したら、割り算の結果を表示して終了。
# 以下の条件を満たすこと：
# 条件	内容
# A	ValueError（数字以外） → 「数字を入力してください」と再入力
# B	ZeroDivisionError（0割） → 「0では割り算できません」と再入力
# C	成功時にのみ結果を出して終了
# D	while True: でループを作り、成功時に break で抜ける

while True: # ループを開始
    try:
        number_a = int(input("1つ目の数字を入力してください: "))    # 1つ目の数字を入力してもらい、整数に変換して変数に格納する
        number_b = int(input("2つ目の数字を入力してください: "))    # 2つ目の数字を入力してもらい、整数に変換して変数に格納する
        new_number = number_a / number_b    # 割り算をおこない、答えを変数に格納する
    except ZeroDivisionError:   # 2つ目の値に「0」が入力された場合のエラーメッセージ
        print("2つ目の数字は、1以上を入力してください。")
        continue    # アプリを中断せずに再入力へ戻す
    except ValueError:  # 数字以外が入力された場合のエラーメッセージ
        print("数字を入力してください。")
        continue    # アプリを中断せずに再入力へ戻す
    else:   # エラーが発生しなかった場合、計算した答えを出力する
        print(f"答えは{new_number}です。")
        break   # 計算が完了したらwhileループを終了する