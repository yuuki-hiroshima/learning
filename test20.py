# 🎯 お題（要件のみ）
# 	1.	数字のリスト [3, 8, 12, 5, 20, 7] を用意する。
# 	2.	for文で1つずつ取り出し、
# 　もし10以上なら「○○は10以上」と表示する。
# 	3.	それ以外の数字はスキップして何も表示しない。

numbers = [3, 8, 12, 5, 20, 7]  # 数字のリスト

for number in numbers:  # 数字のリストから1つずつ数字を取り出す
    if number >= 10:    # 取り出した数字が10以上かチェック
        print(f"{number}は10以上")  # 10以上であれば結果を出力