# 🔹 お題（要件のみ）
# 	1.	リスト ["Aさん", "Bさん", "Cさん"] を用意する。
# 	2.	for文と enumerate() を使って、
# 　「1人目：Aさん」「2人目：Bさん」…のように順番つきで表示する。
# 	3.	コメントは話し言葉で、処理の流れを説明する。

member = ["Aさん", "Bさん", "Cさん"] # リスト

for num, i in enumerate(member, start=1):   # リスト内の値と番号を取得（1からカウント）
    print(f"{num}人目：{i}")    # 取得した順番に「〇人目：〇〇さん」と表示する