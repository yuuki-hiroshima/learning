# データ項目（概念レベル）
# 	•	カード
# 	•	ID（内部一意）
# 	•	カテゴリ（例：Python標準/データ処理/Web/テスト など）
# 	•	タグ（複数可：dict, list, 正規表現 など）
# 	•	表（問題）：ライブラリ名や関数名、あるいは用途を問う短文
# 	•	裏（答え）：名称、短い説明、よくある引数の要点、注意点
# 	•	参考トピック（任意の短いキーワード列）
# 	•	作成日／更新日
# 	•	学習統計：出題回数、正解回数、直近の正誤履歴、連続正解数
# 	•	次回の目安出題日（後述の出題間隔ロジック用）
# 	•	メタ情報
# 	•	既定カテゴリ一覧、利用回数、最終バックアップ日時
# 	•	学習ログ（別管理）
# 	•	日時、モード（ランダム／弱点優先／カテゴリ指定）、出題数、正解率

# コア機能（必須）
# 	1.	カード登録
# 	•	入力値のバリデーション（空文字／極端に長い文字／重複表裏）。
# 	•	既存重複検知（「表」一致で警告→上書き/中止の選択）。
# 	2.	一覧・検索
# 	•	全件／カテゴリ別／タグフィルタ／キーワード検索（表・裏・タグ対象）。
# 	•	並び替え：作成日／更新日／正解率／出題間隔の近さ。
# 	3.	出題（練習セッション）
# 	•	モード：
# 	•	ランダム
# 	•	弱点優先（正解率が低い／直近で誤答した）
# 	•	期日優先（次回目安日が近い順）
# 	•	カテゴリ／タグ指定
# 	•	1問ごとに「表示→回答→自己採点（正／誤）」の入力。
# 	•	結果に応じて学習統計を更新。
# 	4.	編集／削除
# 	•	任意項目の編集、カード削除（取り消しできるよう直前バックアップ推奨）。
# 	5.	統計・ダッシュボード（テキスト）
# 	•	総カード数、出題回数、総合正解率、カテゴリ別正解率トップ3/ワースト3。
# 	•	直近7日・30日の学習量（日次出題数と正解率の推移を文字ベースで可視化）。
# 	6.	保存・復元（JSON）
# 	•	起動時：ファイル存在チェック／スキーマ簡易検証→不整合なら自動バックアップ退避＋初期化。
# 	•	終了時：一時ファイルへの書き出し→成功後置き換え。
# 	•	手動バックアップ／インポート（別名保存・読み込み）。

