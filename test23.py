# 🔹 お題（要件のみ）
# 	1.	ユーザーに数字を入力してもらう。
# 	2.	その数字で「100 ÷ 入力値」の計算をする。
# 	3.	ゼロを入力された場合でもエラーで止まらず、
# 　「0では割り算できません」と表示して続行できるようにする。

number = int(input("数字を入力してください: ")) # ユーザーに数字を入力してもらい、整数に変換してから変数に格納する

try:
    new_number = 100 / number       # 入力された数字を100で割る
    print(f"答えは{new_number}です。")
except ZeroDivisionError:           # 0が入力された場合、エラーメッセージを出力する
    print("0以上の数字を入力してください。")