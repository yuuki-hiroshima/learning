# 🔹 お題（要件のみ）
# ユーザーに 2つの数字 を入力してもらう。
# その2つの数字で割り算を行い、結果を表示する。
# ただし、以下の条件をすべて満たすこと。
# 条件	内容
# A	0で割るときは ZeroDivisionError として「0では割り算できません」
# B	数字以外を入力したときは ValueError として「数字を入力してください」
# C	成功したときだけ結果を表示する（else:を使用）
# D	エラーが出ても最後に「計算を終了します」と出力（finally:を使用）

try:
    number_a = int(input("1つ目の数字を入力してください: "))    # 1つ目の数字を入力してもらい整数に変換してから変数に格納する
    number_b = int(input("2つ目の数字を入力してください: "))    # 2つ目の数字を入力してもらい整数に変換してから変数に格納する
    new_number = number_a / number_b    # 入力された文字を使って割り算をおこない、新しい変数に格納する
except ZeroDivisionError:   # 割る数が0だった場合、エラーが発生するためメッセージを表示する
    print("0では割り算ができません。")
except ValueError:     # 数字以外が入力された場合、エラーが発生するためメッセージを表示する
    print("数字を入力してください。")
else:   # エラーが発生しなかった（正常に計算できた）場合、結果を出力する。
    print(f"答えは{new_number}です。")
finally:    # 計算できた、できないにかかわらず、終了のメッセージを表示する。
    print("計算を終了します。")