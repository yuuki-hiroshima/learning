# 🔹 お題（要件のみ）
# 	1.	数字のリストを作る（例：[10, 0, 5, "a", 20]）
# 	2.	for文でリストの中身を1つずつ取り出し、
# 　各要素に対して「100 ÷ 要素」を試みる。
# 	3.	ただし、次のように条件分けする：
# 　- もし割り算が成功したら、結果を表示
# 　- 0のとき → 「0では割り算できません」
# 　- 文字などで計算できないとき → 「数字以外が含まれています」
# 	4.	どんな要素が来てもプログラムが止まらないようにする。

number = [10, 0, 5, "a", 20]    # リスト

for num in number:  # リストから値を順番に取り出す
    try:
        new_number = 100 / num  # 100を取り出した値で割る
        print(f"100 ÷ {num} = {new_number}です。")
    except ZeroDivisionError:   # 0が入力された場合のエラーメッセージ
        print("0以上の数字を入力してください。")
    except TypeError:   # 数字以外が入力された場合のエラーメッセージ
        print("数字以外が含まれています。")
    except Exception:   # その他、予測できないようなエラーが起きた場合のメッセージ
        print("予期しないエラーが発生しました。")